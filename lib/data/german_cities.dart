  static List<GermanCity> searchCities(String query) {
    if (query.isEmpty) return cities;
    
    final lowercaseQuery = query.toLowerCase();
    return cities.where((city) {
      return city.name.toLowerCase().contains(lowercaseQuery) ||
             city.displayName.toLowerCase().contains(lowercaseQuery) ||
             city.stateAbbreviation.toLowerCase().contains(lowercaseQuery);
    }).toList();
  }

  static GermanCity? findCity(String cityName, String state) {
    final matches = cities.where(
      (city) => city.name == cityName && city.state == state,
    );
    return matches.isNotEmpty ? matches.first : null;
  }

  static GermanCity? findByDisplayName(String displayName) {
    final matches = cities.where(
      (city) => city.displayName == displayName,
    );
    return matches.isNotEmpty ? matches.first : null;
  }
